-- ========================================================
-- 2. สร้างตารางฐานข้อมูลทั้งหมด (Database Schema)
-- ========================================================

-- 2.1 ตารางอาคารหอพัก (เพิ่มประเภท หอชาย/หญิง/รวม)
CREATE TABLE dorms (
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    dorm_type ENUM('หอพักชาย', 'หอพักหญิง', 'หอพักรวม') NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- 2.2 ตารางห้องพัก
CREATE TABLE rooms (
    id INT AUTO_INCREMENT PRIMARY KEY,
    dorm_id INT NOT NULL,
    room_number VARCHAR(10) NOT NULL,
    status VARCHAR(50) DEFAULT 'พร้อมใช้งาน',
    FOREIGN KEY (dorm_id) REFERENCES dorms(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- 2.3 ตารางข้อมูลนักศึกษา (ผูกบัญชี LINE)
CREATE TABLE students (
    student_id VARCHAR(20) PRIMARY KEY,
    name VARCHAR(150) NOT NULL,
    phone VARCHAR(20) NOT NULL,
    room_id INT NOT NULL,
    line_uid VARCHAR(100) UNIQUE DEFAULT NULL,
    line_profile_img VARCHAR(255) DEFAULT NULL,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY (room_id) REFERENCES rooms(id) ON DELETE RESTRICT
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- 2.4 ตารางรายการอุปกรณ์ (Master Data)
CREATE TABLE repair_items_master (
    id INT AUTO_INCREMENT PRIMARY KEY,
    category ENUM('ประปา', 'ไฟฟ้า', 'ซ่อมสร้าง') NOT NULL,
    item_name VARCHAR(150) NOT NULL,
    require_quantity TINYINT(1) DEFAULT 0, 
    is_active TINYINT(1) DEFAULT 1,       
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- 2.5 ตารางเจ้าหน้าที่และช่าง
CREATE TABLE staff (
    id INT AUTO_INCREMENT PRIMARY KEY,
    username VARCHAR(50) UNIQUE DEFAULT NULL,
    password VARCHAR(255) DEFAULT NULL,    
    line_uid VARCHAR(100) UNIQUE DEFAULT NULL,
    name VARCHAR(150) NOT NULL,
    phone VARCHAR(20) DEFAULT NULL,
    role ENUM('admin', 'technician') NOT NULL,
    specialty ENUM('ประปา', 'ไฟฟ้า', 'ซ่อมสร้าง', 'ทั้งหมด') DEFAULT 'ทั้งหมด',
    status ENUM('active', 'inactive') DEFAULT 'active',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- 2.6 ตารางใบงานแจ้งซ่อมหลัก
CREATE TABLE repair_requests (
    id INT AUTO_INCREMENT PRIMARY KEY,
    ticket_id VARCHAR(20) NOT NULL UNIQUE,     
    room_id INT NOT NULL,                      
    student_id VARCHAR(20) DEFAULT NULL,       
    reporter_type ENUM('student', 'admin', 'other') DEFAULT 'student',
    reporter_name VARCHAR(150) NOT NULL,       
    reporter_phone VARCHAR(20) DEFAULT NULL,   
    additional_details TEXT,                   
    status VARCHAR(50) DEFAULT 'รอดำเนินการ',     
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (room_id) REFERENCES rooms(id),
    FOREIGN KEY (student_id) REFERENCES students(student_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- 2.7 ตารางเก็บรูปภาพงานซ่อม
CREATE TABLE repair_images (
    id INT AUTO_INCREMENT PRIMARY KEY,
    request_id INT NOT NULL,
    image_url VARCHAR(255) NOT NULL,           
    uploaded_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (request_id) REFERENCES repair_requests(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- 2.8 ตารางแยกรายการซ่อมรายชิ้น
CREATE TABLE repair_items (
    id INT AUTO_INCREMENT PRIMARY KEY,
    request_id INT NOT NULL,
    item_master_id INT NOT NULL,
    quantity INT DEFAULT 1,
    status VARCHAR(50) DEFAULT 'รอดำเนินการ',     
    FOREIGN KEY (request_id) REFERENCES repair_requests(id) ON DELETE CASCADE,
    FOREIGN KEY (item_master_id) REFERENCES repair_items_master(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ติดตามการเปลี่ยนสถานะรายการอุปกรณ์
CREATE TABLE repair_item_logs (
    id INT AUTO_INCREMENT PRIMARY KEY,
    item_id INT NOT NULL,
    request_id INT NOT NULL,
    item_name VARCHAR(150) NOT NULL,
    old_status VARCHAR(50) DEFAULT NULL,
    new_status VARCHAR(50) NOT NULL,
    staff_id INT NOT NULL,
    staff_name VARCHAR(150) NOT NULL,
    changed_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (item_id) REFERENCES repair_items(id) ON DELETE CASCADE,
    FOREIGN KEY (request_id) REFERENCES repair_requests(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ติดตามการเปลี่ยนสถานะใบงานหลัก
CREATE TABLE repair_request_logs (
    id INT AUTO_INCREMENT PRIMARY KEY,
    request_id INT NOT NULL,
    old_status VARCHAR(50) DEFAULT NULL,
    new_status VARCHAR(50) NOT NULL,
    staff_id INT NOT NULL,
    staff_name VARCHAR(150) NOT NULL,
    changed_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (request_id) REFERENCES repair_requests(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;


