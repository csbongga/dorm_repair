-- ========================================================
-- 1. เคลียร์ตารางเดิมทั้งหมด (ป้องกัน Error ซ้ำซ้อน)
-- ========================================================
SET FOREIGN_KEY_CHECKS = 0;
DROP TABLE IF EXISTS repair_items;
DROP TABLE IF EXISTS repair_images;
DROP TABLE IF EXISTS repair_requests;
DROP TABLE IF EXISTS repair_items_master;
DROP TABLE IF EXISTS students;
DROP TABLE IF EXISTS staff;
DROP TABLE IF EXISTS rooms;
DROP TABLE IF EXISTS dorms;
DROP PROCEDURE IF EXISTS AutoGenerateDormRooms;
